
public class Movie {
   private Integer year;
   private String title;
   private Integer id;

   public void setYear(Integer year) {
      this.year = year;
   }
   public Integer getYear() {
      return year;
   }
   public void setTitle(String title) {
      this.title = title;
   }
   public String getTitle() {
      return title;
   }
   public void setId(Integer id) {
      this.id = id;
   }
   public Integer getId() {
      return id;
   }
}